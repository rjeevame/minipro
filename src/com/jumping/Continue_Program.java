package com.jumping;

public class Continue_Program {
	
	public static void main(String[] args) {
		
		
		for (int i = 0; i <=20; i++) {
			
		if (i==3) {
			
			continue;
		}
		if (i==6) {
			
			continue;
		}
		if (i==9) {
			
			continue;
		}
		if (i==12) {
			
			continue;
		}
		if (i==15) {
			
			continue;
		}
		if (i==18) {
			
			continue;
		}
		System.out.println(i);
		}
	}

}
